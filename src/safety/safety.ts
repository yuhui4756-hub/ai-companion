const highRiskPatterns = [
  "自杀",
  "不想活",
  "结束生命",
  "伤害自己",
  "割腕",
  "轻生",
  "杀了",
  "报复",
];

export function hasHighRiskContent(input: string): boolean {
  return highRiskPatterns.some((pattern) => input.includes(pattern));
}

